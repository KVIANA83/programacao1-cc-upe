# 1. Diferença entre for e while:
# 'for' é usado quando sabemos o número de iterações.
# 'while' é usado quando não sabemos quantas vezes o loop será executado.

# 2. Circunstâncias:
# Use 'for' quando houver uma contagem conhecida.
# Use 'while' quando a repetiçãoo depender de uma condição que será verificada durante a execução.